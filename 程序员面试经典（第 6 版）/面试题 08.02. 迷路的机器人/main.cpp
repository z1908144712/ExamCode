class Solution {
public:
    vector<vector<int>> res;
    int r, c;
    bool helper(vector<vector<int>>& obstacleGrid, int x, int y) {
        if (x >= r || y >= c) {
            return false;
        }
        if (obstacleGrid[x][y] == 1) {
            return false;
        }
        obstacleGrid[x][y] = 1;
        res.push_back({x, y});
        if (x == r - 1 && y == c - 1) {
            return true;
        }
        if (helper(obstacleGrid, x + 1, y) || helper(obstacleGrid, x, y + 1)) {
            return true;
        }
        res.pop_back();
        //obstacleGrid[x][y] = 0;
        return false;
    }

    vector<vector<int>> pathWithObstacles(vector<vector<int>>& obstacleGrid) {
        r = obstacleGrid.size();
        c = obstacleGrid[0].size();
        helper(obstacleGrid, 0, 0);
        return res;
    }
};

